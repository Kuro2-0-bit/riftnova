export const getAbilityImage = (champId, key) => {
  const slug = slugs[champId]
  if (!slug) return null
  const keyMap = { P: 'passive', Q: 'q', W: 'w', E: 'e', R: 'r' }
  const keySlug = keyMap[key]
  if (!keySlug) return null
  return `${BASE}/${slug}/${keySlug}.png`
}